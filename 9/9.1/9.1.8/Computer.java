public class Computer
{
    private int screenSize;
    private int memory;

    
    public Computer() {
        
    }
    
    public void setScreenSize(int ss) {
        screenSize = ss;
    }
    
    public void setMemory(int m) {
        memory = m;
    }
    
    public int getScreenSize() {
        return screenSize;
    }
    
    public int getMemory() {
        return memory;
    }
    
}
