public class Pet extends Animal
{
    private String name;
    private String size;
    
    
    public String getName() {
        return name;
    }
    
    public void setName(String s) {
        name = s;
    }
    
    public String getSize() {
        return size;
    }
    
    public void setSize(String s) {
        size = s;
    }
}