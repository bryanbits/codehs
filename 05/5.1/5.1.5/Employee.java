public class Employee
{
    private String fName;
    private String lName;
    private int salary;
    private double shiftLength;
    
    public Employee(String fName, String lName, int salary, int shiftLength) {
        this.fName = fName;
        this.lName = lName;
        this.salary = salary;
        this.shiftLength = shiftLength;
    }
    
}